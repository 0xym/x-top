    public static final String KEY_NOTIFICATION_ENABLED = "notification_enabled";

    private void updateNotificationState(SharedPreferences sharedPreferences)
    {
        boolean running =  sharedPreferences.getBoolean(KEY_NOTIFICATION_ENABLED, true);
        Intent serviceIntent = new Intent(this, NotificationUpdate.class);
        if (running)
        {
            startService(serviceIntent);
        }
        else
        {
            stopService(serviceIntent);
        }
    }

    @Override
    public void onSharedPreferenceChanged(SharedPreferences sharedPreferences, String key) {
        if (key.equals(KEY_NOTIFICATION_ENABLED)) {
            updateNotificationState(sharedPreferences);
        }
    }

